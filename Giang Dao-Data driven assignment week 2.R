  ## Why was data driven decision making (3DM) useful in this business case?
  
  Data-driven decision-making (DDD) refers to the practice of basing decisions on the
analysis of data, rather than purely on intuition (Provost & Fawcett, 2013). In this case, Amazon has made use of big data in understanding the activities and purchasing patterns of customers. The online platform then analyzes these data to form an algorithm that makes suggestions to customers with items that are most close to the customers' interest.

## How did the organization apply 3DM?

As businesses are collecting more data
than required for any use (McAfee et al., 2012), Amazon also follows this trend in their process of applying data-driven decision making. Amazon records all actions that customers perform on their website, ranging from viewing an item, rating and adding the item in their shopping carts. With such a large amount of data collected from customer behavior, Amazon is able to recommend items a customer might be interested in through comparing all visitors' choices or personalized user-based information.

